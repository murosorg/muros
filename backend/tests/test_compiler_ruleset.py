def test_icmp_rule_covers_both_families(session):
    _add_rule(session, action="accept", protocol="icmp")
    out = compile_ruleset(session)
    assert "meta l4proto { icmp, ipv6-icmp }" in out


def test_portless_tcp_rule_matches_both_families(session):
    _add_rule(session, action="accept", protocol="tcp")
    out = compile_ruleset(session)
    assert "meta l4proto tcp" in out
    assert "ip protocol tcp" not in out


def test_service_group_multi_protocol_yields_one_line_each(session):
    sg = models.ServiceGroup(name="kerberos")
    sg.ports = [
        models.ServiceGroupPort(protocol="tcp", port="88"),
        models.ServiceGroupPort(protocol="udp", port="88"),
    ]
    session.add(sg)
    session.flush()
    _add_rule(session, action="accept", service_group_id=sg.id)
    out = compile_ruleset(session)
    assert "tcp dport 88" in out
    assert "udp dport 88" in out


def test_rate_limit_is_emitted(session):
    _add_rule(session, action="accept", protocol="tcp", dst_port="22",
              rate_limit="5/minute burst 10")
    out = compile_ruleset(session)
    assert "limit rate 5/minute burst 10 packets" in out


def test_logging_prefix_carries_action_and_rule_id(session):
    r = _add_rule(session, chain="input", action="drop", protocol="tcp",
                  dst_port="23", log=True)
    out = compile_ruleset(session)
    assert f'log prefix "[muros DROP r={r.id} input] "' in out


def test_every_rule_has_counter_and_marker_comment(session):
    r = _add_rule(session, action="accept")
    out = compile_ruleset(session)
    line = next(ln for ln in out.splitlines() if f"[muros r={r.id}]" in ln)
    assert "counter" in line
    assert line.strip().endswith(f'comment "[muros r={r.id}]"')


def test_disabled_rule_is_skipped(session):
    r = _add_rule(session, action="accept", protocol="tcp", dst_port="9999",
                  enabled=False)
    out = compile_ruleset(session)
    assert f"[muros r={r.id}]" not in out
    assert "dport 9999" not in out


def test_rules_are_ordered_by_position(session):
    first = _add_rule(session, action="accept", protocol="tcp", dst_port="111",
                      position=1)
    second = _add_rule(session, action="accept", protocol="tcp", dst_port="222",
                       position=2)
    out = compile_ruleset(session)
    assert out.index(f"[muros r={first.id}]") < out.index(f"[muros r={second.id}]")


def test_nat_masquerade(session):
    eth0 = _iface(session, "eth0")
    n = models.NatRule(type="masquerade", interface_id=eth0.id, enabled=True)
    session.add(n)
    session.flush()
    out = compile_ruleset(session)
    assert "oifname eth0" in out
    assert "masquerade" in out
    assert f"[muros nat={n.id}]" in out


def test_nat_snat_to_explicit_ip(session):
    eth0 = _iface(session, "eth0")
    n = models.NatRule(type="snat", interface_id=eth0.id,
                       redirect_to_ip="203.0.113.9", enabled=True)
    session.add(n)
    session.flush()
    out = compile_ruleset(session)
    assert "snat to 203.0.113.9" in out


def test_nat_dnat_with_explicit_destination(session):
    eth0 = _iface(session, "eth0")
    n = models.NatRule(type="dnat", interface_id=eth0.id,
                       dst_address="203.0.113.1", protocol="tcp", dst_port="443",
                       redirect_to_ip="10.0.0.5", redirect_to_port="8443",
                       enabled=True)
    session.add(n)
    session.flush()
    out = compile_ruleset(session)
    assert "ip daddr 203.0.113.1" in out
    assert "tcp dport 443" in out
    assert "dnat to 10.0.0.5:8443" in out


def test_nat_dnat_without_destination_excludes_firewall_ips(session):
    _iface(session, "eth1", ip="192.168.1.1/24")
    eth0 = _iface(session, "eth0")
    n = models.NatRule(type="dnat", interface_id=eth0.id, protocol="tcp",
                       dst_port="443", redirect_to_ip="10.0.0.5", enabled=True)
    session.add(n)
    session.flush()
    out = compile_ruleset(session)
    assert "ip daddr != 192.168.1.1" in out
    assert "dnat to 10.0.0.5" in out


