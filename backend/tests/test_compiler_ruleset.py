def test_wireguard_disabled_adds_nothing(session):
    session.add(models.WireGuardConfig(id=1, enabled=False,
                                       interface_name="wg0",
                                       address_cidr="10.10.0.1/24"))
    session.flush()
    out = compile_ruleset(session)
    assert "wg0" not in out


def test_wireguard_enabled_adds_forward_and_masquerade(session):
    session.add(models.WireGuardConfig(id=1, enabled=True,
                                       interface_name="wg0",
                                       address_cidr="10.10.0.1/24"))
    session.flush()
    out = compile_ruleset(session)
    assert 'iifname "wg0" accept' in out
    assert 'oifname "wg0" accept' in out
    assert "tcp option maxseg size set rt mtu" in out
    assert 'ip saddr 10.10.0.0/24 oifname != "wg0" masquerade' in out
