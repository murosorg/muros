# Show or clear the persisted firmware action log. On FreeBSD this was handled
# by opnsense-update -G (show) and -es (clear); on Debian the log is a plain
# file written by config.sh, so we read or truncate it directly.
case "${1}" in
clear)
	: > "${LOGFILE}" 2>/dev/null || true
	;;
show|*)
	if [ -f "${LOGFILE}" ]; then
		cat "${LOGFILE}"
	fi
	;;
esac
